namespace GameFrameX.DiscoveryCenterManager.Player;

/// <summary>
/// 玩家信息接口，定义玩家唯一标识
/// </summary>
public interface IPlayerInfo
{
    /// <summary>
    /// 玩家唯一ID，全局唯一，用于标识玩家身份
    /// </summary>
    long PlayerId { get; }

    /// <summary>
    /// 服务器ID，用于标识玩家所属的服务器
    /// </summary>
    long ServerId { get; }

    /// <summary>
    /// 服务器实例ID，用于标识玩家所在的具体服务器实例（支持多实例部署）
    /// </summary>
    long ServerInstanceId { get; }
}