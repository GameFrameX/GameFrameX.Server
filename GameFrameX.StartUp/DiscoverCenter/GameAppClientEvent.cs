using GameFrameX.NetWork.Messages;

namespace GameFrameX.StartUp.DiscoverCenter;

/// <summary>
/// 机器人TCP客户端事件结构体,包含各种回调事件
/// </summary>
internal sealed class GameAppClientEvent
{
    /// <summary>
    /// 连接成功时的回调
    /// </summary>
    public Action OnConnected;

    /// <summary>
    /// 连接关闭时的回调
    /// </summary>
    public Action OnClosed;

    /// <summary>
    /// 发生错误时的回调
    /// </summary>
    public Action<SuperSocket.ClientEngine.ErrorEventArgs> OnError;

    /// <summary>
    /// 接收到消息时的回调
    /// </summary>
    public Action<MessageObject> OnMessage;
}